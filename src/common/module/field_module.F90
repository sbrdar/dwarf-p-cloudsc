    ENDIF
    PTR(LBOUNDS(1):, LBOUNDS(2):, LBOUNDS(3):, LBOUNDS(4):, LBOUNDS(5):) => SELF%DEVPTR(:,:,:,:,:)
    IF (IAND (MODE, NWR) /= 0) THEN
