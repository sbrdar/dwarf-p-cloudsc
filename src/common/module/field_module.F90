    CLASS(FIELD_LOG3D_OWNER) :: SELF
    INTEGER(KIND=JPIM), INTENT(IN) :: LBOUNDS(3)
    INTEGER(KIND=JPIM), INTENT(IN) :: UBOUNDS(3)
