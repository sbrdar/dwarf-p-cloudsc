  SUBROUTINE FIELD_LOG5D_GET_HOST_DATA (SELF, MODE, PTR)
    CLASS(FIELD_LOG5D), INTENT (INOUT) :: SELF
    INTEGER (KIND=JPIM),                INTENT (IN) :: MODE
    LOGICAL, POINTER, INTENT(INOUT) :: PTR(:,:,:,:,:)
